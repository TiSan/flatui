	private static HashMap<String, Font> fonts = new HashMap<>();

	/**
	 * Returns the current instance of FlatIconFont.
	 * 
	 * @return current font instance
	 */
	public static Font getInstance() {
		return getInstance(24, Font.PLAIN);
	}

	public static Font getInstance(int size, int style) {
		// Font f = Font.getFont("Consolas");
		try {
			String s = size + "";
			if (fonts.containsKey(s)) {
				return fonts.get(s);
			}
			String tmpPath = System.getProperty("java.io.tmpdir");
			if(tmpPath.length() > 0){
				tmpPath += "/";
			}
			File f = new File(tmpPath + "FlatUI/");
			if (!f.exists()) {
				f.mkdirs();
			}
			File fi = new File(tmpPath + "FlatUI/fa_4.6.3.ttf");
			if (!fi.exists()) {
				Files.copy(FlatFont.class.getResourceAsStream("/de/tisan/resources/fa_4.6.3.ttf"), fi.toPath());
			}
			Font ff = Font.createFont(Font.TRUETYPE_FONT, fi);
			Font fon = ff.deriveFont(style, size);
			fonts.put(s, fon);
			return fon;
		} catch (FontFormatException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
		return null;

	}
